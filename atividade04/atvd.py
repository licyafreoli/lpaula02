nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))

if nota1 >= 6 and nota2 >= 6:
    print("O aluno está aprovado, ambas as notas são maiores ou iguais a 6.")
else:
    print("O aluno não está aprovado, uma ou ambas as notas são menores que 6.")
